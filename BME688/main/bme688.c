int bmi_get_chipid(void) {
    uint8_t reg_id = 0x00;
    uint8_t tmp;

    bmi_i2c_read(I2C_NUM_0, &reg_id, &tmp, 1);
    printf("Valor de CHIPID: %2X \n\n", tmp);

    if (tmp == 0x24) {
        printf("Chip BMI270 reconocido.\n\n");
        return 0;
    } else {
        printf("Chip BMI270 no reconocido. \nCHIP ID: %2x\n\n", tmp);  // %2X
    }

    return 1;
}

